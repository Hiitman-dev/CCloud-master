package com.pira.ccloud.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Standard spacing scale for consistent layout across the app.
 * Use these constants instead of hardcoded dp values.
 */
object Spacing {
    /** 4dp - Extra small spacing */
    val xs = 4.dp

    /** 8dp - Small spacing */
    val sm = 8.dp

    /** 12dp - Medium spacing */
    val md = 12.dp

    /** 16dp - Large spacing (standard horizontal padding) */
    val lg = 16.dp

    /** 20dp - Extra large spacing */
    val xl = 20.dp

    /** 24dp - Double extra large spacing */
    val xxl = 24.dp

    /** 32dp - Triple extra large spacing */
    val xxxl = 32.dp

    /** 48dp - Section spacing */
    val section = 48.dp

    /** 100dp - Bottom bar clearance */
    val bottomBarClearance = 100.dp

    /** 8dp - Top bar breathing room */
    val topBarClearance = 8.dp
}
